#include<iostream>
using namespace std;
int main()
{
	int i,j,k,c=1,t,mat[100][100],l,m;
	cin>>t;
	l=0;
	m=t-1;
	for(i=l;i<t;i++)
	{
		for(j=l;j<=m;j++)
		{
			mat[i][j]=c;
			c++;
		}
		l++;
		for(j=l;j<=m;j++)
		{
			mat[j][m]=c;
			c++;
		}
		for(j=(m-1);j>=(l-1);j--)
		{
			mat[m][j]=c;
			c++;
		}
		m--;
		l--;
		for(j=m;j>=(l+1);j--)
		{
			mat[j][l]=c;
			c++;
		}
		l++;
	}
	cout<<"[";
	for(i=0;i<t;i++)
	{
		cout<<"[";
		for(j=0;j<t;j++)
		{
			if(j!=(t-1))
			{
				cout<<mat[i][j]<<","<<'\t';
			}
			else
				cout<<mat[i][j];		
		}
		if(i!=(t-1))
		{
			cout<<"],";
			cout<<'\n';
		}
		else
			cout<<"]";
		
	}
	cout<<"]";
}
			
